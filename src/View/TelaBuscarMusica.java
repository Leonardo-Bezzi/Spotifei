    /**
     * Método chamado ao clicar no botão "Buscar".
     * Realiza a consulta no banco de dados com base no filtro e termo informados.
     * Preenche a tabela com os resultados encontrados.
     * 
     * @param evt o evento de clique no botão
     */
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        //Faz a busca de acordo com o filtro selecionado
        String termo = tfTermo.getText();
        String filtro = cbFiltro.getSelectedItem().toString().toLowerCase();

        String campoFiltro = switch (filtro) {
            case "nome" -> "nome";
            case "artista" -> "artista";
            case "gênero" -> "genero";
            default -> "nome";
        };

        MusicaDAO dao = new MusicaDAO();
        List<Musica> resultados = dao.buscarPorCampo(campoFiltro, termo, idUsuario);

        DefaultTableModel model = (DefaultTableModel) tblResultados.getModel();
        model.setRowCount(0);

        for (Musica m : resultados) {
            model.addRow(new Object[]{
                m.getId(),
                m.getNome(),
                m.getArtista(),
                m.getGenero(),
                m.getDuracao(),
                m.isCurtida()  
            });
        }   
    }//GEN-LAST:event_btnBuscarActionPerformed

